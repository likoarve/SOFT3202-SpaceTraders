package model.objects.user;

public class UserInfo {
    String username;
    int shipCount;
    int structureCount;
    String joinedAt;
    int credits;

    public String getUsername() {
        return username;
    }

    public int getShipCount() {
        return shipCount;
    }

    public int getStructureCount() {
        return structureCount;
    }

    public String getJoinedAt() {
        return joinedAt;
    }

    public int getCredits() {
        return credits;
    }
}
